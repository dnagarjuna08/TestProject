def bitSelection(workstationName, ETCode, TMCode):
	bits = Project_VECV.Transmission.bitSelection.getBits()
	try:
		bitSelectionDataset = system.db.runNamedQuery("Project_VECV/Transmission/Poka-yoke/getBitSelection",{"workStationName":workstationName,"ETCodeId":ETCode,"TMCodeId":TMCode})
	except:
		return []
